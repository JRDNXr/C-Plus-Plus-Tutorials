#include <iostream>
#include <string> // The string library (talked more about later)

using namespace std;

int main() {
    cout << "Hello \t There"; // Puts a tab between the words
    cout << endl;
    cout << "Hello \nThere"; // Puts the word on a newline
    cout << endl;
    cout << "Hello \v There"; // Adds a vertical indentation
    cout << endl;
    cout << "\'Hello There\'"; // Adds single quotes
    cout << endl;
    cout << "\"Hello There\""; // Adds double quotes
    cout << endl;
    cout << "\\ Hello There \\"; // Adds character backslash
    cout << endl;
}
