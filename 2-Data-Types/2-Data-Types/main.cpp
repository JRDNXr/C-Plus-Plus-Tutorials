#include <iostream>

using namespace std;

int main() {
    
    int integer = 2; // Whole numbers
    double decimal = 2.2; // Also known as floating-point numbers
    char character = 'A'; // Stores 1 character
    string str =  "JRDN"; // Can store an entire length of characters
    bool checker = true; // A true or false statement (0 = false | 1 = true)
    
    cout << integer << endl;
    cout << decimal << endl;
    cout << character << endl;
    cout << str << endl;
    cout << checker << endl; // This will output a number, NOT "true"
    
}
